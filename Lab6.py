'''    
Q1.Print the following pattern
     *
    **
   ***
  ****
 *****  
 '''

rows = int(input("enter number of rows : "))

#condition for printing the pattern
i = 1
while i <= rows:
     print(" " * (rows - i) + "*" * i)
     i = i+1

