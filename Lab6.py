'''
Q2. Print the following pattern
10101
 0101
  101
   01
    1
'''

rows = int(input("Enter the number of rows: "))

i = 0
while i < rows:
    print(" " * i, end="")  # Print leading spaces
    j = 0
    while j < (rows - i):
        print((i + j) % 2, end="")  # Print alternating 1s and 0s
        j += 1
    print()  # Move to the next line
    i += 1

