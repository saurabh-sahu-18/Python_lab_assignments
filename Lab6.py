'''
Q3. Print the following pattern
1
10
101
1010
10101
'''

rows = int(input("Enter the number of rows: "))

i = 1
while i <= rows:
    j = 0
    while j < i:
        print((j % 2), end="")  # Print 1 and 0 alternately
        j += 1
    print()  # Move to the next line
